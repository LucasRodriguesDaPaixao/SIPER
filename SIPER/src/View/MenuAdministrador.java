     public void carregarlista(){
        DefaultTableModel modelo = (DefaultTableModel) tblLproduto.getModel();
        modelo.setNumRows(0);
           
        DAO.DaoAdministrador itensCliente = new DaoAdministrador();
        ResultSet dados = itensCliente.listar();

        try{
            //função para verificar o tanto de itens que tem em "dados"
            while(dados.next()){
                modelo.addRow(new Object[]{
                    dados.getString(3),
                    dados.getString(2),
                    dados.getString(4)
                });
            }
        }catch(SQLException error){
           throw new RuntimeException(error);
        }
    }

